; STANDARD HEADER FILE
	PROCESSOR 16F876A
; --- REGISTER FILES 선언 ---
; BANK 0
INDF	EQU	00H
TMR0	EQU	01H
PCL	EQU	02H
STATUS	EQU	03H
FSR	EQU	04H
PORTA	EQU	05H
PORTB	EQU	06H
PORTC	EQU	07H
PCLATH	EQU	0AH
INTCON	EQU	0BH
; BANK 1
OPTIONR	EQU	81H
TRISA	EQU	85H
TRISB	EQU	86H
TRISC	EQU	87H
ADCON1	EQU	9FH
; --- STATUS BITS선언 ---
IRP	EQU	7
RP1	EQU	6
RP0	EQU	5
NOT_TO	EQU	4
NOT_PD	EQU	3
ZF	EQU	2	;ZERO FLAG BIT
DC	EQU	1	;DIGIT CARRY/BORROW BIT
CF	EQU	0	;CARRY/BORROW FLAG BIT
; --INTCON BITS 선언 ---
;다음에 채우기 
; --OPTION BITS 선언 ---

W	EQU	B'0'	;W 변수를 0으로 선언
F	EQU	.1	;F 변수를 1로 선언

;사용자 정의 변수
DISP1	EQU	21H
DISP2	EQU	22H
DISP3	EQU	23H
DISP4	EQU	24H
DBUF1 EQU	25H
DBUF2	EQU	26H
; MAIN PROGRAM
	ORG 0000
	BSF	STATUS,RP0	;BANK를 1로 변경함
	MOVLW	B'00000111'	;PORTA를 DIGITAL I/O로사용
	MOVWF	ADCON1
	MOVLW	B'00001111'	;
	MOVWF	TRISA
	MOVLW	B'00000000'
	MOVWF	TRISC
	BCF	STATUS,RP0	;BANK를 0으로 변경
;
	MOVLW B'00001011'
	MOVWF PORTC
LP    MOVF PORTA,W
	ANDLW B'00001111'
      SUBLW B'00001010'
   	BTFSC STATUS,ZF
   	GOTO BUZZON
      GOTO BUZZOFF

BUZZON
	BSF PORTA,5
	GOTO LP
BUZZOFF
	BCF PORTA,5
	GOTO LP
	
	END