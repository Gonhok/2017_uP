	PROCESSOR 16F876A
; --- REGISTER FILES 선언 ---
; BANK 0
INDF	EQU	00H
TMR0	EQU	01H
PCL	EQU	02H
STATUS	EQU	03H
FSR	EQU	04H
PORTA	EQU	05H
PORTB	EQU	06H
PORTC	EQU	07H
PCLATH	EQU	0AH
INTCON	EQU	0BH
; BANK 1
OPTIONR	EQU	81H
TRISA	EQU	85H
TRISB	EQU	86H
TRISC	EQU	87H
ADCON1	EQU	9FH
; --- STATUS BITS선언 ---
IRP	EQU	7
RP1	EQU	6
RP0	EQU	5
NOT_TO	EQU	4
NOT_PD	EQU	3
ZF	EQU	2	;ZERO FLAG BIT
DC	EQU	1	;DIGIT CARRY/BORROW BIT
CF	EQU	0	;CARRY/BORROW FLAG BIT
; --INTCON BITS 선언 ---
;다음에 채우기 
; --OPTION BITS 선언 ---

W	EQU	B'0'	;W 변수를 0으로 선언
F	EQU	.1	;F 변수를 1로 선언

;사용자 정의 변수
DISP1	EQU	21H
DISP2	EQU	22H
DISP3	EQU	23H
DISP4	EQU	24H
DBUF1 EQU	25H
DBUF2	EQU	26H
; MAIN PROGRAM
	ORG 0000
	BSF	STATUS,RP0	;BANK를 1로 변경함
	MOVLW	B'00000111'	;PORTA를 DIGITAL I/O로사용
	MOVWF	ADCON1
	MOVLW	B'00000000'	;
	MOVWF	TRISA
	MOVLW	B'00000000'
	MOVWF	TRISC
	BCF	STATUS,RP0	;BANK를 0으로 변경
;
MAIN	CLRF 	PORTA
	CLRF	PORTC
	MOVLW	B'10011111'
	MOVWF	DISP1
	MOVLW	B'00100101'
	MOVWF DISP2
	MOVLW	B'00001101'
	MOVWF DISP3
	MOVLW	B'10011001'
	MOVWF DISP4
	
D_LOOP
	MOVF	DISP1,W ;DISP1
	MOVWF	PORTC
	MOVLW	B'00001000'
	MOVWF	PORTA
	CALL	DELAY
	
	MOVLW	0;DISP2
	MOVWF	PORTA
	MOVF	DISP2,W
	MOVWF	PORTC
	MOVLW	B'00000100'
	MOVWF	PORTA
	CALL	DELAY
	
	MOVLW	0;DISP3
	MOVWF	PORTA
	MOVF	DISP3,W
	MOVWF	PORTC
	MOVLW	B'00000010'
	MOVWF	PORTA
	CALL	DELAY
	
	MOVLW	0;DISP4
	MOVWF	PORTA
	MOVF	DISP4,W
	MOVWF	PORTC
	MOVLW	B'00000001'
	MOVWF	PORTA
	CALL	DELAY
	
	MOVLW	0
	MOVWF	PORTA
	GOTO	D_LOOP
	
; SUBROUTINE
DELAY
	MOVLW .6
	MOVWF	DBUF1
LP1	MOVLW	.200
	MOVWF	DBUF2
LP2	NOP
	DECFSZ	DBUF2,F
	GOTO	LP2
	DECFSZ	DBUF1,F
	GOTO	LP1
	RETURN
	
